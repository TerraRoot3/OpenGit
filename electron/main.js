// Git 操作函数
async function executeGitCommand(command, cwd) {
  try {
    if (Array.isArray(command)) {
      const [cmd, ...args] = command
      return await executeCommandArgs(cmd, args, cwd)
    }
    const { stdout, stderr } = await execAsync(command, { cwd })
    return { success: true, stdout: stdout || '', stderr: stderr || '' }
  } catch (error) {
    return { 
      success: false, 
      error: error.message, 
      stdout: error.stdout || '', 
      stderr: error.stderr || '' 
    }
  }
}

// 检查并修正Git仓库的remote地址
async function checkAndFixRemoteUrl(repoPath, correctUrl, projectName) {
  if (!correctUrl || !correctUrl.includes('git@')) {
    return { success: true, message: '', fixed: false }
  }
  
  try {
    safeLog(`🔍 检查项目 ${projectName} 的remote地址`)
    
    // 获取当前remote地址
    const remoteResult = await executeGitCommand(['git', 'remote', 'get-url', 'origin'], repoPath)
    
    if (remoteResult.success && remoteResult.stdout) {
      const currentUrl = remoteResult.stdout.trim()
      safeLog(`📋 当前remote地址: ${currentUrl}`)
      safeLog(`🎯 正确的remote地址: ${correctUrl}`)
      
      if (currentUrl === correctUrl) {
        safeLog(`✅ 远程地址正确: ${projectName}`)
        return { success: true, message: '', fixed: false }
      } else {
        safeLog(`🔧 需要修正remote地址: ${projectName}`)
        
        // 修正remote地址
        const fixResult = await executeGitCommand(['git', 'remote', 'set-url', 'origin', correctUrl], repoPath)
        
        if (fixResult.success) {
          safeLog(`✅ remote地址修正成功: ${projectName}`)
          return { success: true, message: `已修正remote地址: ${correctUrl}`, fixed: true }
        } else {
          safeLog(`❌ remote地址修正失败: ${projectName}`)
          return { success: false, message: `修正remote地址失败: ${fixResult.stderr}`, fixed: false }
        }
      }
    } else {
      // 如果没有origin remote，创建它
      safeLog(`🔧 创建origin remote: ${projectName}`)
      const addRemoteResult = await executeGitCommand(['git', 'remote', 'add', 'origin', correctUrl], repoPath)
      
      if (addRemoteResult.success) {
        safeLog(`✅ origin remote创建成功: ${projectName}`)
        return { success: true, message: `已创建origin remote: ${correctUrl}`, fixed: true }
      } else {
        safeLog(`❌ origin remote创建失败: ${projectName}`)
        return { success: false, message: `创建origin remote失败: ${addRemoteResult.stderr}`, fixed: false }
      }
    }
  } catch (error) {
    safeError(`❌ 检查remote地址失败 ${projectName}:`, error.message)
    return { success: false, message: `检查remote地址失败: ${error.message}`, fixed: false }
  }
}

// 带实时输出的Git命令执行
async function executeGitCommandWithOutput(command, cwd, processCallback = null) {
  return new Promise((resolve) => {
    const { spawn } = require('child_process')

    safeLog(`📡 执行命令: ${command.join(' ')} @ ${cwd}`)

    const windows = BrowserWindow.getAllWindows()
    const mainWindow = windows.find(w => w.webContents.getURL().includes('localhost:5173')) || windows[0]
    const sendRealtimeOutput = (output, type) => {
      if (processCallback) {
        processCallback(output, type)
        return
      }
      if (mainWindow && !mainWindow.isDestroyed()) {
        mainWindow.webContents.send('git-output-update', {
          projectName: 'unknown',
          output,
          type
        })
      }
    }

    const child = spawn(command[0], command.slice(1), {
      cwd: cwd,
      stdio: ['ignore', 'pipe', 'pipe']
    })

    let stdoutData = ''
    let stderrData = ''
    let settled = false
    const timeoutMs = 10 * 60 * 1000
    const timeout = setTimeout(() => {
      if (settled) return
      settled = true
      safeError(`⏰ 命令执行超时 (${timeoutMs / 1000}s): ${command.join(' ')}`)
      try { child.kill('SIGTERM') } catch (e) { /* ignore */ }
      setTimeout(() => {
        try { child.kill('SIGKILL') } catch (e) { /* ignore */ }
      }, 5000)
      resolve({
        success: false,
        code: -1,
        stdout: stdoutData,
        stderr: stderrData || `命令执行超时 (${timeoutMs / 1000}秒)`,
        timeout: true
      })
    }, timeoutMs)

    child.stdout.on('data', (data) => {
      const output = data.toString()
      stdoutData += output
      if (output) {
        sendRealtimeOutput(output, 'stdout')
      }
    })

    child.stderr.on('data', (data) => {
      const output = data.toString()
      stderrData += output
      if (output) {
        sendRealtimeOutput(output, 'stderr')
      }
    })

    child.on('close', (code) => {
      if (settled) return
      settled = true
      clearTimeout(timeout)
      safeLog(`📋 命令执行完成，退出码: ${code}`)
      safeLog(`📋 命令结果: ${code === 0 ? '成功' : '失败'} (${command[0]})`)
      resolve({
        success: code === 0,
        code: code,
        stdout: stdoutData,
        stderr: stderrData
      })
    })

    child.on('error', (error) => {
      if (settled) return
      settled = true
      clearTimeout(timeout)
      safeError(`❌ 命令执行错误: ${error.message}`)
      resolve({
        success: false,
        error: error.message,
        code: -1,
        stdout: stdoutData,
        stderr: stderrData
      })
    })
  })
}

