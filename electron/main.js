app.whenReady().then(async () => {
  safeLog('Electron app ready, creating window...')
  await session.defaultSession.setProxy({ mode: 'system' }).catch((error) => {
    safeError('[Proxy] failed to apply system proxy for default session:', error.message || error)
  })
  webTabManager = new WebTabManager({
    safeLog,
    safeError,
    lifecycleOptions: {
      discardDelayMs: Number.POSITIVE_INFINITY
    },
    webTabPreloadPath: path.join(__dirname, 'web-tab-preload.js')
  })
  sitePermissionManager = createSitePermissionManager({ store })
  sessionPartitionManager = createSessionPartitionManager()

  webTabLifecycleInterval = setInterval(() => {
    if (!webTabManager) return
    const transitions = webTabManager.advanceLifecycle(Date.now())
    if (!Array.isArray(transitions) || transitions.length === 0) return

    for (const transition of transitions) {
      const [tabId, phase] = String(transition).split(':')
      if (!tabId || !phase) continue
      if (phase === 'discarded') {
        webTabManager.discardWebTab(tabId)
      }
    }
  }, 1000)

  if (typeof webTabLifecycleInterval.unref === 'function') {
    webTabLifecycleInterval.unref()
  }

  // 注册 local-resource:// 协议：安全地将 ~/.gitManager/ 下的文件提供给渲染进程
  // URL 格式：local-resource://相对路径  例：local-resource://screenshots/screenshot-123.png
  const gitManagerBase = path.resolve(os.homedir(), '.gitManager')
  session.defaultSession.protocol.handle('local-resource', (request) => {
    try {
      const url = new URL(request.url)
      // url.host + url.pathname 拼合出相对路径，例如 "screenshots/screenshot-123.png"
      const relPath = decodeURIComponent((url.host || '') + url.pathname)
      const fullPath = path.resolve(gitManagerBase, relPath)
      // 安全校验：只允许访问 ~/.gitManager/ 下的文件
      if (!fullPath.startsWith(gitManagerBase + path.sep) && fullPath !== gitManagerBase) {
        return new Response('Forbidden', { status: 403 })
      }
      return net.fetch(pathToFileURL(fullPath).toString())
    } catch (e) {
      return new Response('Internal Error', { status: 500 })
    }
  })

  configureBrowserSessionPartition(SITE_PERMISSION_PARTITION)
  
  // 设置存储路径（确保使用应用数据目录）
  const userDataPath = app.getPath('userData')
  safeLog(`📁 User data path: ${userDataPath}`)
  safeLog(`✅ Webview session configured with persist:main`)
  
  createWindow()

}).catch((error) => {
  safeError('Error in app.whenReady():', error)
})

