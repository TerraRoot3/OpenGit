function quoteShellArg(value) {
  return "'" + String(value).replace(/'/g, "'\\''") + "'"
}

function ensurePathEntries(pathValue, requiredEntries) {
  const delimiter = path.delimiter
  const currentParts = (pathValue || '')
    .split(delimiter)
    .map((part) => part.trim())
    .filter(Boolean)
  const existing = new Set(currentParts)
  const missing = requiredEntries.filter((entry) => !existing.has(entry))
  return [...currentParts, ...missing].join(delimiter)
}

let cachedSystemProxyEnv = null
let cachedSystemProxyEnvAt = 0
const SYSTEM_PROXY_ENV_TTL = 15 * 1000

function parseScutilProxyOutput(rawOutput = '') {
  const values = {}
  rawOutput
    .split('\n')
    .map((line) => line.trim())
    .filter(Boolean)
    .forEach((line) => {
      const match = line.match(/^([A-Za-z0-9]+)\s*:\s*(.+)$/)
      if (!match) return
      values[match[1]] = match[2]
    })
  return values
}

function buildProxyUrl({ scheme = 'http', host = '', port = '', username = '', password = '' } = {}) {
  if (!host || !port) return ''
  const auth = username
    ? `${encodeURIComponent(username)}${password ? `:${encodeURIComponent(password)}` : ''}@`
    : ''
  return `${scheme}://${auth}${host}:${port}`
}

function normalizeQuotedValue(value = '') {
  return String(value || '').trim().replace(/^'+|'+$/g, '').replace(/^"+|"+$/g, '')
}

function normalizeNoProxyList(values = []) {
  const out = []
  for (const raw of values) {
    const value = String(raw || '').trim()
    if (!value) continue
    if (value === '<local>') {
      out.push('localhost', '127.0.0.1')
      continue
    }
    out.push(value)
  }
  return [...new Set(out)].join(',')
}

function getMacSystemProxyEnv() {
  if (process.platform !== 'darwin') {
    return {}
  }

  const now = Date.now()
  if (cachedSystemProxyEnv && (now - cachedSystemProxyEnvAt) < SYSTEM_PROXY_ENV_TTL) {
    return { ...cachedSystemProxyEnv }
  }

  try {
    const output = execFileSync('scutil', ['--proxy'], {
      encoding: 'utf8',
      stdio: ['ignore', 'pipe', 'ignore']
    })
    const proxyConfig = parseScutilProxyOutput(output)
    const env = {}

    const httpProxy = Number(proxyConfig.HTTPEnable) === 1
      ? buildProxyUrl({
        scheme: 'http',
        host: proxyConfig.HTTPProxy,
        port: proxyConfig.HTTPPort,
        username: proxyConfig.HTTPUser,
        password: proxyConfig.HTTPPassword
      })
      : ''

    const httpsProxy = Number(proxyConfig.HTTPSEnable) === 1
      ? buildProxyUrl({
        scheme: 'http',
        host: proxyConfig.HTTPSProxy,
        port: proxyConfig.HTTPSPort,
        username: proxyConfig.HTTPSUser,
        password: proxyConfig.HTTPSPassword
      })
      : ''

    const socksProxy = Number(proxyConfig.SOCKSEnable) === 1
      ? buildProxyUrl({
        scheme: 'socks5',
        host: proxyConfig.SOCKSProxy,
        port: proxyConfig.SOCKSPort
      })
      : ''

    const noProxy = typeof proxyConfig.ExceptionsList === 'string'
      ? proxyConfig.ExceptionsList.replace(/[()"]/g, '').split(/\s*,\s*|\s+/).filter(Boolean).join(',')
      : ''

    if (httpProxy) {
      env.http_proxy = httpProxy
      env.HTTP_PROXY = httpProxy
    }

    if (httpsProxy || httpProxy) {
      const resolvedHttpsProxy = httpsProxy || httpProxy
      env.https_proxy = resolvedHttpsProxy
      env.HTTPS_PROXY = resolvedHttpsProxy
    }

    if (socksProxy) {
      env.all_proxy = socksProxy
      env.ALL_PROXY = socksProxy
    }

    if (noProxy) {
      env.no_proxy = noProxy
      env.NO_PROXY = noProxy
    }

    cachedSystemProxyEnv = env
    cachedSystemProxyEnvAt = now
    return { ...env }
  } catch (error) {
    cachedSystemProxyEnv = {}
    cachedSystemProxyEnvAt = now
    return {}
  }
}

function parseWindowsProxyServer(rawValue = '') {
  const text = String(rawValue || '').trim()
  if (!text) return {}

  const assignments = text.includes('=')
    ? text.split(';').map((item) => item.trim()).filter(Boolean)
    : [`http=${text}`, `https=${text}`]

  let httpProxy = ''
  let httpsProxy = ''
  let allProxy = ''

  for (const item of assignments) {
    const [rawKey, rawTarget] = item.includes('=') ? item.split('=') : ['http', item]
    const key = String(rawKey || '').trim().toLowerCase()
    const target = String(rawTarget || '').trim()
    if (!target) continue

    const buildPlainProxy = (scheme) => {
      try {
        const parsed = new URL(target)
        return buildProxyUrl({
          scheme,
          host: parsed.hostname,
          port: parsed.port || (scheme === 'http' ? '80' : ''),
          username: decodeURIComponent(parsed.username || ''),
          password: decodeURIComponent(parsed.password || '')
        })
      } catch {
        const [host, port] = target.split(':')
        return buildProxyUrl({ scheme, host, port })
      }
    }

    if (key === 'http') httpProxy = buildPlainProxy('http')
    else if (key === 'https') httpsProxy = buildPlainProxy('http')
    else if (key === 'socks') allProxy = buildPlainProxy('socks5')
  }

  return { httpProxy, httpsProxy, allProxy }
}

function getWindowsSystemProxyEnv() {
  if (process.platform !== 'win32') {
    return {}
  }

  const now = Date.now()
  if (cachedSystemProxyEnv && (now - cachedSystemProxyEnvAt) < SYSTEM_PROXY_ENV_TTL) {
    return { ...cachedSystemProxyEnv }
  }

  try {
    const output = execFileSync('reg', ['query', 'HKCU\\Software\\Microsoft\\Windows\\CurrentVersion\\Internet Settings'], {
      encoding: 'utf8',
      stdio: ['ignore', 'pipe', 'ignore']
    })
    const values = {}
    output
      .split('\n')
      .map((line) => line.trim())
      .filter(Boolean)
      .forEach((line) => {
        const match = line.match(/^([A-Za-z0-9_]+)\s+REG_[A-Z0-9_]+\s+(.+)$/)
        if (!match) return
        values[match[1]] = String(match[2] || '').trim()
      })

    if (Number(values.ProxyEnable) !== 1) {
      cachedSystemProxyEnv = {}
      cachedSystemProxyEnvAt = now
      return {}
    }

    const { httpProxy, httpsProxy, allProxy } = parseWindowsProxyServer(values.ProxyServer)
    const noProxy = normalizeNoProxyList(String(values.ProxyOverride || '').split(';'))
    const env = {}

    if (httpProxy) {
      env.http_proxy = httpProxy
      env.HTTP_PROXY = httpProxy
    }
    if (httpsProxy || httpProxy) {
      const resolvedHttpsProxy = httpsProxy || httpProxy
      env.https_proxy = resolvedHttpsProxy
      env.HTTPS_PROXY = resolvedHttpsProxy
    }
    if (allProxy) {
      env.all_proxy = allProxy
      env.ALL_PROXY = allProxy
    }
    if (noProxy) {
      env.no_proxy = noProxy
      env.NO_PROXY = noProxy
    }

    cachedSystemProxyEnv = env
    cachedSystemProxyEnvAt = now
    return { ...env }
  } catch (error) {
    cachedSystemProxyEnv = {}
    cachedSystemProxyEnvAt = now
    return {}
  }
}

function getLinuxSystemProxyEnv() {
  if (process.platform !== 'linux') {
    return {}
  }

  const now = Date.now()
  if (cachedSystemProxyEnv && (now - cachedSystemProxyEnvAt) < SYSTEM_PROXY_ENV_TTL) {
    return { ...cachedSystemProxyEnv }
  }

  try {
    const execGsettings = (schema, key) => execFileSync('gsettings', ['get', schema, key], {
      encoding: 'utf8',
      stdio: ['ignore', 'pipe', 'ignore']
    }).trim()

    const mode = normalizeQuotedValue(execGsettings('org.gnome.system.proxy', 'mode'))
    if (mode !== 'manual') {
      cachedSystemProxyEnv = {}
      cachedSystemProxyEnvAt = now
      return {}
    }

    const httpHost = normalizeQuotedValue(execGsettings('org.gnome.system.proxy.http', 'host'))
    const httpPort = normalizeQuotedValue(execGsettings('org.gnome.system.proxy.http', 'port'))
    const httpsHost = normalizeQuotedValue(execGsettings('org.gnome.system.proxy.https', 'host'))
    const httpsPort = normalizeQuotedValue(execGsettings('org.gnome.system.proxy.https', 'port'))
    const socksHost = normalizeQuotedValue(execGsettings('org.gnome.system.proxy.socks', 'host'))
    const socksPort = normalizeQuotedValue(execGsettings('org.gnome.system.proxy.socks', 'port'))
    const ignoreHostsRaw = normalizeQuotedValue(execGsettings('org.gnome.system.proxy', 'ignore-hosts'))
    const ignoreHosts = ignoreHostsRaw
      ? ignoreHostsRaw.replace(/^\[|\]$/g, '').split(',').map((item) => normalizeQuotedValue(item)).filter(Boolean)
      : []

    const env = {}
    if (httpHost && httpPort) {
      const proxy = buildProxyUrl({ scheme: 'http', host: httpHost, port: httpPort })
      env.http_proxy = proxy
      env.HTTP_PROXY = proxy
    }
    if (httpsHost && httpsPort) {
      const proxy = buildProxyUrl({ scheme: 'http', host: httpsHost, port: httpsPort })
      env.https_proxy = proxy
      env.HTTPS_PROXY = proxy
    } else if (env.http_proxy) {
      env.https_proxy = env.http_proxy
      env.HTTPS_PROXY = env.HTTP_PROXY
    }
    if (socksHost && socksPort) {
      const proxy = buildProxyUrl({ scheme: 'socks5', host: socksHost, port: socksPort })
      env.all_proxy = proxy
      env.ALL_PROXY = proxy
    }

    const noProxy = normalizeNoProxyList(ignoreHosts)
    if (noProxy) {
      env.no_proxy = noProxy
      env.NO_PROXY = noProxy
    }

    cachedSystemProxyEnv = env
    cachedSystemProxyEnvAt = now
    return { ...env }
  } catch (error) {
    cachedSystemProxyEnv = {}
    cachedSystemProxyEnvAt = now
    return {}
  }
}

function getSystemProxyEnv() {
  if (process.platform === 'darwin') return getMacSystemProxyEnv()
  if (process.platform === 'win32') return getWindowsSystemProxyEnv()
  if (process.platform === 'linux') return getLinuxSystemProxyEnv()
  return {}
}

function buildTerminalLaunchOptions({ shell, resolvedCwd, homeDir }) {
  const shellBase = path.basename(shell).replace(/\.exe$/i, '').toLowerCase()
  const requiredUnixPathEntries = [
    '/usr/local/bin',
    '/opt/homebrew/bin',
    '/usr/local/sbin',
    '/opt/homebrew/sbin',
    '/usr/bin',
    '/bin',
    '/usr/sbin',
    '/sbin'
  ]
  const env = {
    ...process.env,
    PWD: resolvedCwd,
    OLDPWD: homeDir,
    TERM: 'xterm-256color',
    COLORTERM: 'truecolor',
    LANG: process.env.LANG || 'en_US.UTF-8',
    TERM_PROGRAM: 'gitmanager'
  }

  if (process.platform !== 'win32') {
    env.PATH = ensurePathEntries(env.PATH, requiredUnixPathEntries)
  }

  const systemProxyEnv = getSystemProxyEnv()
  Object.assign(env, systemProxyEnv)

  if (process.platform === 'win32') {
    return { shellArgs: [], env, shellBase }
  }

  if (shellBase === 'zsh') {
    const { zshDir } = ensureTerminalBootstrapFiles()
    env.GIT_MANAGER_INITIAL_CWD = resolvedCwd
    env.GIT_MANAGER_REAL_ZDOTDIR = process.env.ZDOTDIR || homeDir
    env.ZDOTDIR = zshDir
    return { shellArgs: ['-il'], env, shellBase }
  }

  if (shellBase === 'bash') {
    const { bashRcPath } = ensureTerminalBootstrapFiles()
    env.GIT_MANAGER_INITIAL_CWD = resolvedCwd
    env.GIT_MANAGER_REAL_BASHRC = path.join(homeDir, '.bashrc')
    return { shellArgs: ['--rcfile', bashRcPath, '-i'], env, shellBase }
  }

  if (shellBase === 'fish') {
    env.GIT_MANAGER_INITIAL_CWD = resolvedCwd
    return { shellArgs: ['-i', '-C', `cd -- ${quoteShellArg(resolvedCwd)}`], env, shellBase }
  }

  return { shellArgs: [], env, shellBase }
}

// ============ 性能优化：git fetch 去重 ============
// 同一仓库短时间内只执行一次 git fetch，避免重复网络请求
const _fetchLocks = new Map() // key: repoPath, value: Promise
const _fetchTimestamps = new Map() // key: repoPath, value: timestamp
const FETCH_DEDUP_INTERVAL = 30 * 1000 // 30秒内不重复 fetch
const FETCH_CACHE_CLEANUP_INTERVAL = 5 * 60 * 1000 // 5 分钟清理一次过期条目

// 定期清理过期的 fetch 缓存条目，防止内存泄漏
setInterval(() => {
  const now = Date.now()
  for (const [key, timestamp] of _fetchTimestamps) {
    if (now - timestamp > FETCH_CACHE_CLEANUP_INTERVAL) {
      _fetchTimestamps.delete(key)
      _fetchLocks.delete(key)
    }
  }
}, FETCH_CACHE_CLEANUP_INTERVAL)

// ============ 性能优化：git status 短期缓存 ============
// 同一仓库 2 秒内复用上次 status 结果，避免同一刷新周期内重复调用
const _statusCache = new Map() // key: repoPath, value: { result, timestamp }
const STATUS_CACHE_TTL = 2000 // 2 秒

async function cachedGitStatus(repoPath) {
  const cached = _statusCache.get(repoPath)
  if (cached && Date.now() - cached.timestamp < STATUS_CACHE_TTL) {
    return cached.result
  }
  const result = await executeGitCommand(['git', 'status', '--porcelain'], repoPath)
  _statusCache.set(repoPath, { result, timestamp: Date.now() })
  return result
}

async function deduplicatedFetch(repoPath) {
  const now = Date.now()
  const lastFetch = _fetchTimestamps.get(repoPath) || 0

  // 如果 30 秒内已经 fetch 过，直接跳过
  if (now - lastFetch < FETCH_DEDUP_INTERVAL) {
    return { success: true, stdout: '', stderr: '', skipped: true }
  }

  // 如果有正在进行的 fetch，复用同一个 Promise
  if (_fetchLocks.has(repoPath)) {
    return _fetchLocks.get(repoPath)
  }

  const fetchPromise = executeGitCommand(['git', 'fetch', 'origin', '--quiet'], repoPath)
    .then(result => {
      _fetchTimestamps.set(repoPath, Date.now())
      _fetchLocks.delete(repoPath)
      return result
    })
    .catch(err => {
      _fetchLocks.delete(repoPath)
      return { success: false, error: err.message, stdout: '', stderr: '' }
    })

  _fetchLocks.set(repoPath, fetchPromise)
  return fetchPromise
}

// ============ 性能优化：带并发限制的批量执行 ============
async function promiseAllWithLimit(tasks, limit = 5) {
  const results = []
  let index = 0

  async function runNext() {
    const i = index++
    if (i >= tasks.length) return
    results[i] = await tasks[i]()
    await runNext()
  }

  const workers = Array.from({ length: Math.min(limit, tasks.length) }, () => runNext())
  await Promise.all(workers)
  return results
}

