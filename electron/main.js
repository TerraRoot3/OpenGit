// 检查并处理 Git 锁文件
async function checkAndRemoveGitLock(cwd) {
  try {
    const lockPath = path.join(cwd, '.git', 'index.lock')
    
    // 检查锁文件是否存在
    if (!fs.existsSync(lockPath)) {
      return { removed: false, reason: 'no_lock' }
    }
    
    safeLog('⚠️ 检测到 Git 锁文件:', lockPath)
    
    // 检查锁文件的修改时间（如果超过1分钟，认为是僵尸锁文件）
    const stats = fs.statSync(lockPath)
    const ageSeconds = (Date.now() - stats.mtime.getTime()) / 1000
    
    // 锁文件较新时严格保守，不自动删除
    if (ageSeconds < 120) {
      return { removed: false, reason: 'lock_recent', age: ageSeconds }
    }

    // 锁文件较旧（>=120s），仅在确认没有持有进程时删除
    try {
      const { execSync } = require('child_process')

      const lsofResult = execSync(`lsof "${lockPath}" 2>/dev/null || true`, { encoding: 'utf-8', timeout: 1200 })
      if (lsofResult.trim()) {
        safeLog('⏳ 检测到进程正在持有 lock 文件，跳过删除')
        return { removed: false, reason: 'file_in_use', age: ageSeconds }
      }

      safeLog(`🔓 未检测到持有进程，删除陈旧 lock 文件 (${ageSeconds.toFixed(1)}s)`)
      fs.unlinkSync(lockPath)
      return { removed: true, reason: 'stale_lock_no_owner', age: ageSeconds }
    } catch (error) {
      // lsof 不可用或检查失败时，严格保守：不删除
      safeLog('⚠️ 无法确认 lock 文件持有者，保守跳过删除:', error.message)
      return { removed: false, reason: 'cannot_verify_owner', age: ageSeconds }
    }
  } catch (error) {
    safeError('❌ 处理 Git 锁文件失败:', error.message)
    return { removed: false, reason: 'error', error: error.message }
  }
}

